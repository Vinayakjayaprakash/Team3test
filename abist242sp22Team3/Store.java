package ist242;


public class Store {

    // Property
    private String storename;

    // Constructor Method
    public Store(String newStoreName){
        storename = newStoreName;
    }

    // Getter Method
    public String getStorename() {
        return this.storename;
    }

    // Setter Method
    public void setStorename(String newStoreName) {
        this.storename = newStoreName;
    }


}
